const hasDeveloperJob = true;

if (hasDeveloperJob) {
  console.log("Timmy is employed as a developer.");
}

const isTimmyAGamer = false;

if (isTimmyAGamer) {
  console.log("Timmy loves to play World of Warcraft.");
}

const timmyAge = 15;

if (timmyAge >= 16) {
  console.log("Timmy is old enough to drive.");
} else {
  console.log("Timmy is not old enough to drive.");
}
// In this code, we have defined three boolean variables: hasDeveloperJob, isTimmyAGamer, and timmyAge. We then use if statements to check the values of these variables and print appropriate messages based on their values. The first two conditions check for true or false values, while the third condition checks if Timmy's age is greater than or equal to 16 to determine if he is old enough to drive.